class InitialData {
    final String  initialHealth;
    final String  initialAmmo;
    final String  initialGun;
    final String  walkSpeed;

    public InitialData(String initialHealth, String initialAmmo, String initialGun, String walkSpeed) {
        this.initialHealth = initialHealth;
        this.initialAmmo = initialAmmo;
        this.initialGun = initialGun;
        this.walkSpeed = walkSpeed;
    }
}
